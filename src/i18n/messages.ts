export const SUPPORTED_APP_LOCALES = ['pt-BR', 'en-US', 'es-ES'] as const

export type AppLocale = (typeof SUPPORTED_APP_LOCALES)[number]

export function resolveAppLocale(rawLocale?: string | null): AppLocale {
  const normalized = String(rawLocale ?? '')
    .trim()
    .toLowerCase()
    .replace('_', '-')

  if (normalized === 'en-us') return 'en-US'
  if (normalized === 'es-es') return 'es-ES'
  return 'pt-BR'
}

